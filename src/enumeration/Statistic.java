package enumeration;

/**
 * this enum defines the statistics of athletes
 * 
 * @author Findlay Royds
 * @version 1.0, May 2023.
 */
public enum Statistic {
	/**
	 * How slowly an athlete loses stamina
	 */
	FITNESS,
	/**
	 * How good the athlete is in an offensive position
	 */
	OFFENCE,
	/**
	 * How good the athlete is in a defensive position
	 */
	DEFENCE,
	/**
	 * How good the athlete is at jumping
	 */
	JUMPING,
	/**
	 * How good the athlete is at dribbling
	 */
	DRIBBLING,
	/**
	 * How tall the athlete is
	 */
	HEIGHT,
	/**
	 * How accurate the athlete is at shooting
	 */
	SHOOTING_ACCURACY,
	/**
	 * How far the athlete can shoot a basketball
	 */
	SHOOTING_POWER
}
